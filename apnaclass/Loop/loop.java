public class loop {
    public static void main(String[] args) {
        /* loop in java 
        --  there are three type of loop in java --
        1. for loop
        2. while loop
        3. do while loop
        */

    // 1. for loop 

    int a = 5;
    for(int i=0; i<a; i++){
        System.out.println(" for abhishek");
    }

    // 2. while loop 
     
    int b = 10;
    int c = 0;
    while(c<b){
        System.out.println(" while abhishek");
        c++;
    } 

    // 3. do while loop

    int d =10;
    int e = 0;
    do{
        System.out.println("do abhishek");
        e++;
    }
    while(e<d);
    }
}
