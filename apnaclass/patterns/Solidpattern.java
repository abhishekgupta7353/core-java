package patterns;

public class Solidpattern {
    public static void main(String[] args) {

        /*  (Q.1) solid pattern
               *****
               *****
               *****
               *****
         */
        
        int n =4;
        int m=5;

        for(int i=1; i<=n; i++){
            for(int j=1; j<=m; j++){
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
